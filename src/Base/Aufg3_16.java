package Base;

public class Aufg3_16 {
    public static void main(String[] args) {
        for (int i = 20; i > 0; i -= 2) {
            System.out.print(i + " ");
        }

        System.out.println();

        int i = 20;
        while (i > 0) {
            System.out.print(i + " ");
            i -= 2;
        }
    }
}
